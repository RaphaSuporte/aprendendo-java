package Exercicio10;

public class Ponto {

    private int lucas = 20000;
    private int osvaldo = 10000;

    public Ponto() {
    }

    public int getLucas() {
        return lucas;
    }

    public void setLucas(int lucas) {
        lucas = 20000;
        this.lucas = lucas;
    }

    public int getOsvaldo() {
        osvaldo = 10000;
        return osvaldo;
    }

    public void setOsvaldo(int osvaldo) {
        this.osvaldo = osvaldo;
    }
}



